  fun fib1(N: Int): Int {
    if (N < 1) {
      return 0
    }
    if (N == 1 || N == 2) {
      return 1
    }
    var pre = 1
    var curr = 1
    for (i in 3..N) {
      val sum = pre + curr
      pre = curr
      curr = sum
    }
    return curr
  }

